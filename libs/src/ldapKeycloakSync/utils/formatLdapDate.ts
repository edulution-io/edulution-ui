const formatLdapDate = (d: Date): string => {
  const pad = (n: number) => n.toString().padStart(2, '0');
  return `${
    d.getUTCFullYear() +
    pad(d.getUTCMonth() + 1) +
    pad(d.getUTCDate()) +
    pad(d.getUTCHours()) +
    pad(d.getUTCMinutes()) +
    pad(d.getUTCSeconds())
  }.0Z`;
};

export default formatLdapDate;
