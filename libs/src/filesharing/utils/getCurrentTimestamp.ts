const getCurrentTimestamp = (): string =>
  new Date()
    .toLocaleString('de-DE', {
      year: 'numeric',
      month: '2-digit',
      day: '2-digit',
      hour: '2-digit',
      minute: '2-digit',
      second: '2-digit',
    })
    .replace(/[:.]/g, '-')
    .replace(/, /g, '_');

export default getCurrentTimestamp;
